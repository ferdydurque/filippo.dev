	if ( $post_type == 'page' && $id != 0 ) :
		// pages 
		$args = array( 
			'post_type' => $post_type
			,'page_id' => $id
		);
		$query = new WP_Query($args);
		if ($query->have_posts()) :
			while ($query->have_posts()) : $query->the_post();
				
				the_content();
			
			endwhile;
			wp_reset_postdata();
		else :
			echo $empty;
		endif;
	else:
		// $id == 0 --> the page is the index of last posts ...
		if ( $id == 0 ) $post_type = 'post';
		// posts	
		$args = array( 
			'post_type' => $post_type
			, 'post_status' => 'publish'
			,'p' => $id
		);
		$query = new WP_Query($args);
		if ($query->have_posts()) :
			while ($query->have_posts()) : $query->the_post();
				?>
                <div class="post"> 
                <?php
				
					the_content();
				
				?>
                </div>
                <?php		
			endwhile;
			wp_reset_postdata();
		else :
			echo $empty;
		endif;
	endif;
	die();
}

